            focusedBorder: UnderlineInputBorder(
                borderSide: BorderSide(color: Colors.redAccent)),
            hintText: 'Password',
            icon: new Icon(
              Icons.lock,
              color: Colors.redAccent,
            )),
        validator: (value) => value.isEmpty ? 'Password can\'t be empty' : null,
        onSaved: (value) => _password = value.trim(),
      ),
    );
  }

